// ============================================
// fetch-live-data.js — 预取 football-data.org 数据
// 生成 live-scores.json 和 live-data-inline.js
// 用法: node fetch-live-data.js
// ============================================

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const API_KEY = process.env.FD_API_KEY || '';
const OUTPUT_JSON = path.join(__dirname, 'live-scores.json');
const OUTPUT_JS = path.join(__dirname, 'live-data-inline.js');

// 使用curl而非Node.js https模块（解决socket hang up问题）
function fetchAPI(apiPath) {
  const url = `https://api.football-data.org/v4${apiPath}`;
  try {
    const result = execSync(
      `curl -s -H "X-Auth-Token: ${API_KEY}" "${url}"`,
      { encoding: 'utf-8', timeout: 30000, maxBuffer: 5 * 1024 * 1024 }
    );
    return JSON.parse(result);
  } catch (e) {
    throw new Error(`curl failed: ${e.message}`);
  }
}

async function main() {
  console.log('=== WC2026 Live Data Fetcher ===');
  const now = new Date();
  const bjNow = new Date(now.getTime() + 8 * 3600000);
  console.log(`Time (UTC): ${now.toISOString()}`);
  console.log(`Time (BJ):  ${bjNow.toISOString()}`);
  console.log('');

  let bestData = null;

  // 只使用WC（世界杯），不使用2003（那是荷甲联赛）
  try {
    console.log('Fetching competition WC (FIFA World Cup)...');
    bestData = fetchAPI('/competitions/WC/matches?status=FINISHED,IN_PLAY,PAUSED,TIMED,SCHEDULED&limit=80');
    if (bestData.matches && bestData.matches.length > 0) {
      console.log(`✅ WC: ${bestData.matches.length} matches`);
    } else {
      console.log('❌ WC: 0 matches returned');
    }
  } catch (e) {
    console.log(`❌ WC failed: ${e.message}`);
  }

  // 如果WC失败，尝试日期范围查询
  if (!bestData || !bestData.matches || bestData.matches.length === 0) {
    try {
      const today = now.toISOString().split('T')[0];
      const weekLater = new Date(now.getTime() + 7 * 86400000).toISOString().split('T')[0];
      console.log(`Fetching all matches from ${today} to ${weekLater}...`);
      bestData = fetchAPI(`/matches?dateFrom=${today}&dateTo=${weekLater}`);
      if (bestData.matches && bestData.matches.length > 0) {
        console.log(`✅ Date range: ${bestData.matches.length} matches`);
      }
    } catch (e) {
      console.log(`❌ Date range failed: ${e.message}`);
    }
  }

  // 最终回退：获取所有今天比赛
  if (!bestData || !bestData.matches || bestData.matches.length === 0) {
    try {
      const today = now.toISOString().split('T')[0];
      console.log(`Fetching today's matches (${today})...`);
      bestData = fetchAPI(`/matches?dateFrom=${today}&dateTo=${today}`);
      if (bestData.matches && bestData.matches.length > 0) {
        console.log(`✅ Today: ${bestData.matches.length} matches`);
      }
    } catch (e) {
      console.log(`❌ Today failed: ${e.message}`);
    }
  }

  if (!bestData || !bestData.matches) {
    console.error('❌ No data fetched from API!');
    process.exit(1);
  }

  // 处理比赛数据 — 精简为网站需要的格式
  const matches = bestData.matches.map(m => ({
    homeTeam: m.homeTeam?.shortName || m.homeTeam?.name || 'Unknown',
    awayTeam: m.awayTeam?.shortName || m.awayTeam?.name || 'Unknown',
    status: m.status,
    utcDate: m.utcDate,
    score: {
      fullTime: m.score?.fullTime || { home: null, away: null },
      halfTime: m.score?.halfTime || { home: null, away: null },
    },
    minute: m.minute || null,
    venue: m.venue || null,
  }));

  // 分类统计
  const finished = matches.filter(m => m.status === 'FINISHED');
  const inPlay = matches.filter(m => ['IN_PLAY', 'PAUSED', 'LIVE'].includes(m.status));
  const scheduled = matches.filter(m => ['TIMED', 'SCHEDULED'].includes(m.status));

  const output = {
    fetchTime: now.toISOString(),
    fetchTimeBeijing: bjNow.toISOString().replace('T', ' ').substring(0, 19) + ' (北京时间)',
    total: matches.length,
    finished: finished.length,
    inPlay: inPlay.length,
    scheduled: scheduled.length,
    matches: matches,
  };

  // 写JSON文件
  fs.writeFileSync(OUTPUT_JSON, JSON.stringify(output, null, 2), 'utf-8');

  // 写内联JS文件
  const jsContent = `// AUTO-GENERATED by fetch-live-data.js — DO NOT EDIT\n// Data fetched: ${output.fetchTimeBeijing}\nwindow.WC_LIVE_DATA = ${JSON.stringify(output)};\n`;
  fs.writeFileSync(OUTPUT_JS, jsContent, 'utf-8');

  console.log('');
  console.log(`✅ JSON: ${OUTPUT_JSON}`);
  console.log(`✅ Inline JS: ${OUTPUT_JS}`);
  console.log(`   Total: ${output.total} | Finished: ${output.finished} | In-Play: ${output.inPlay} | Scheduled: ${output.scheduled}`);
  console.log('');

  // 显示正在进行的比赛
  if (inPlay.length > 0) {
    console.log('🔴 LIVE MATCHES:');
    for (const m of inPlay) {
      const hg = m.score.fullTime.home ?? m.score.halfTime.home ?? 0;
      const ag = m.score.fullTime.away ?? m.score.halfTime.away ?? 0;
      console.log(`   ${m.homeTeam} ${hg} - ${ag} ${m.awayTeam} (min: ${m.minute || '?'})`);
    }
  }

  // 显示最近完赛
  console.log('✅ RECENT FINISHED:');
  for (const m of finished.slice(-5)) {
    console.log(`   ${m.homeTeam} ${m.score.fullTime.home}-${m.score.fullTime.away} ${m.awayTeam}`);
  }
}

main().catch(e => {
  console.error('Fatal error:', e);
  process.exit(1);
});